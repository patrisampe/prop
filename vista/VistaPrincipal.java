    public static java.util.Date convierteStringADate(String stringFecha) {
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy"); 
            java.util.Date fecha = sdf.parse(stringFecha); 
            return fecha; 
        }catch(Exception e) { 
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error en la conversion de fechas" + e); 
            return null; 
        }
    }
    
