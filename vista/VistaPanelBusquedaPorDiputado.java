    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        if (jComboBox2.getSelectedIndex() >= 0) {
            String key = jComboBox2.getSelectedItem().toString();
            if (importancias.containsKey(key))
                jComboBox5.setSelectedItem(importancias.get(key).toString());
            else
                jComboBox5.setSelectedItem(ctrlEve.getImportanciaTipoEvento(key).toString());
        }
    }//GEN-LAST:event_jComboBox2ActionPerformed

    private void jComboBox5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox5ActionPerformed
        if (jComboBox2.getSelectedIndex() >= 0) {
            String key = jComboBox2.getSelectedItem().toString();
            if (importancias.containsKey(key)) importancias.remove(key);
            Integer valor = Integer.parseInt(jComboBox5.getSelectedItem().toString());
            if (!ctrlEve.getImportanciaTipoEvento(key).equals(valor))
                importancias.put(jComboBox2.getSelectedItem().toString(), valor);
        }
    }//GEN-LAST:event_jComboBox5ActionPerformed


