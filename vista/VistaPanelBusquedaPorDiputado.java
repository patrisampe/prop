    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Integer indiceAfinidad = jSlider5.getValue();
        TipoAlgoritmo tipo = TipoAlgoritmo.valueOf(jComboBox1.getSelectedItem().toString());
        HashMap<Criterio, Double> criterios = new HashMap<>();
        criterios.put(Criterio.Standard, Double.parseDouble(String.valueOf(jSlider1.getValue()))/5000);
        criterios.put(Criterio.Estado, Double.parseDouble(String.valueOf(jSlider2.getValue()))/5000);
        criterios.put(Criterio.ParecidoNombres, Double.parseDouble(String.valueOf(jSlider3.getValue()))/5000);
        criterios.put(Criterio.PartidoPolitico, Double.parseDouble(String.valueOf(jSlider4.getValue()))/5000);
        Integer lapsoDeTiempo = Integer.parseInt(jComboBox3.getSelectedItem().toString());
        String diputadoRelevante = jComboBox4.getSelectedItem().toString();
        if (ctrlRes.existeResultado("ProvisionalNonUser")) {
            ctrlRes.removeResultado("ProvisionalNonUser");
        }
        ctrlRes.nuevoResultadoPorDiputado(indiceAfinidad, tipo, importancias, lapsoDeTiempo, diputadoRelevante, criterios);
        ctrlRes.registraUltimoResultado("ProvisionalNonUser");
        if (ctrlRes.getIDsPorDiputado("ProvisionalNonUser").size() > 0) {
            VistaResultadoPorDiputado visResDip = new VistaResultadoPorDiputado(vista, "ProvisionalNonUser");
            visResDip.setVisible(true);
        }
        else System.out.println("No hay grupos en el resultado");
    }//GEN-LAST:event_jButton3ActionPerformed

