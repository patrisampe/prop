    public FormConsultarEvento(VistaEvento vista, String tipo, String nombre) {
        initComponents();
        this.vista = vista;
        setLocationRelativeTo(null);
        setModal(true);
        ctrlDip = ControladorDominioDiputado.getInstance();
        ctrlEve = ControladorDominioEvento.getInstance();
        nombresEve = new ArrayList<>(ctrlEve.getEventos(tipo));
        iter = nombresEve.listIterator();
        String nombre_actual = nombre + "noEqual";
        while (iter.hasNext() && !nombre_actual.equals(nombre)) {
            nombre_actual = iter.next();
        }
        rellenaCampos(tipo, nombre);
    }
    
    private void rellenaCampos(String tipo, String nombre) {
        jTextField1.setText(nombre);
        jTextField2.setText(tipo);
        jTextField3.setText(ctrlEve.getFechaEvento(tipo, nombre).toString());
        jComboBox1.removeAllItems();
        Set<String> nombres = ctrlEve.getDiputadosEvento(tipo, nombre);
        for (String nom: nombres) {
            jComboBox1.addItem(nom);
        }
    }
        
