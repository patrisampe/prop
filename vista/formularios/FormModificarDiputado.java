    private void rellenaCampos(String nombre) {
        jTextField1.setText(nombre);
        jTextField2.setText(ctrlDip.getPartidoPolitico(nombre));
        jTextField3.setText(ctrlDip.getEstado(nombre));
        jDateChooser1.setDate(VistaPrincipal.convierteStringADate(ctrlDip.getFechaDeNacimiento(nombre).toString()));
        rellenaCamposLegislaturas();
    }
    
    private void rellenaCamposLegislaturas() {
        String nombre = jTextField1.getText();
        Set<Integer> idsleg = ctrlLeg.getIDs();
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        for (Integer id:idsleg) {
            if (ctrlDip.existsLegistura(nombre, id)) jComboBox1.addItem(ctrlLeg.getFechaInicio(id) + "-" + ctrlLeg.getFechaFinal(id));
            else jComboBox2.addItem(ctrlLeg.getFechaInicio(id) + "-" + ctrlLeg.getFechaFinal(id));
        }
    }
        
