    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Date data_nacimiento = Date.parseDate(sdf.format(jDateChooser1.getDate()));
        ctrlDip.setPartidoPolitico(jTextField1.getText(), jTextField2.getText());
        ctrlDip.setEstado(jTextField1.getText(), jTextField3.getText());
        ctrlDip.setFechaDeNacimiento(jTextField1.getText(), data_nacimiento);
        vista.rellenaDiputados();
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jComboBox2.getSelectedIndex() >= 0) {
            Integer idleg = ctrlLeg.getID(Date.parseDate(jComboBox2.getSelectedItem().toString().substring(0, 10)));
            ctrlDip.addLegistura(jTextField1.getText(), idleg);
            rellenaCamposLegislaturas();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jComboBox1.getItemCount() > 1) {
            Integer idleg = ctrlLeg.getID(Date.parseDate(jComboBox1.getSelectedItem().toString().substring(0, 10)));
            ctrlDip.removeLegistura(jTextField1.getText(), idleg);
            rellenaCamposLegislaturas();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

